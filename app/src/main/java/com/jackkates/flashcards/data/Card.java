package com.jackkates.flashcards.data;


public class Card {

    private int id;

    private String term;

    private String definition;

    private int rank;

    public int getID() {
        return id;
    }

    public String getTerm() {
        return term;
    }

    public String getDefinition() {
        return definition;
    }

    public int getRank() {
        return rank;
    }
}


